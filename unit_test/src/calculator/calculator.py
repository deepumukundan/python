class Calculator:
    def add(self, a: int, b: int) -> int:
        return a + b
    
    def substract(self, a: int, b: int) -> int:
        return a - b
    
    def multiply(self, a: int, b: int) -> int:
        return a * b
    
    def divide(self, a: int, b: int) -> float:
        return float(a) / b